public class Candy {

    private String taste;
    private int amount;

    public Candy(String taste, int amount) {

        this.taste = taste;
        this.amount = amount;

    }

    public String getTaste() {
        return taste;
    }

    public void setTaste(String taste) {
        this.taste = taste;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

}
